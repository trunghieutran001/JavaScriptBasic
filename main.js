// Câu lệnh rẽ nhanh - Switch
var date = 3;

switch (date) {
    case 2:
        console.log('Today is monday');
        break;
    case 3:
        console.log('Today is Tuesday');
        break;
    case 4:
        console.log('Today is wednesday');
        break;
    case 5:
        console.log('Today is thursday');
        break;
    case 6:
        console.log('To day is Friday');
        break;
};

//case 2
var year = 5;
switch (year) {
    case 2:
    case 3:
    case 4:
        console.log('2 3 4');
        break;
    case 5:
        console.log('5');
    default:
        console.log('Khong biet');
};

